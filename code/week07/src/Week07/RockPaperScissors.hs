data Game = Game
    { gFirst          :: !PubKeyHash
    , gSecond         :: !PubKeyHash
    , gStake          :: !Integer
    , gPlayDeadline   :: !Slot
    , gRevealDeadline :: !Slot
    , gToken          :: !AssetClass
    } deriving (Show, Generic, FromJSON, ToJSON, Prelude.Eq, Prelude.Ord)

PlutusTx.makeLift ''Game

data GameChoice = Rock
                | Paper
                | Scissors
    deriving (Show, Generic, FromJSON, ToJSON, ToSchema, Prelude.Eq, Prelude.Ord)

instance Eq GameChoice where
    {-# INLINABLE (==) #-}
    Rock  == Rock        = True
    Paper == Paper       = True
    Scissors == Scissors = True
    _ == _               = False
PlutusTx.unstableMakeIsData ''GameChoice

beats :: (GameChoice, GameChoice) -> Bool
beats (Rock, Scissors)  = True
beats (Scissors, Paper) = True
beats (Paper, Rock)     = True
beats _                 = False

type Nonce = ByteString
data GameDatum = GameDatum { gdPlayer :: Nonce, gdMove :: Maybe GameChoice }
               | Finished
    deriving Show

instance Eq GameDatum where
    {-# INLINABLE (==) #-}
    GameDatum bs mc == GameDatum bs' mc' = (bs == bs') && (mc == mc')
    Finished        == Finished          = True
    _               ==  _                = False

PlutusTx.unstableMakeIsData ''GameDatum

data GameRedeemer = Play GameChoice
                  | Reveal { rvPlayer :: Nonce, rvMove :: GameChoice }
                  | ClaimFirst
                  | ClaimSecond
    deriving Show

PlutusTx.unstableMakeIsData ''GameRedeemer

{-# INLINABLE lovelaces #-}
lovelaces :: Value -> Integer
lovelaces = Ada.getLovelace . Ada.fromValue

{-# INLINABLE gameDatum #-}
gameDatum :: TxOut -> (DatumHash -> Maybe Datum) -> Maybe GameDatum
gameDatum o f = do
    dh      <- txOutDatum o
    Datum d <- f dh
    PlutusTx.fromData d

